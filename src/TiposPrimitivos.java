public class TiposPrimitivos {
    public static void main(String[] args) {
        //tipo primitivo não tem método
        int a1 = 10;

        //tipos não primitivos contém métodos
        Integer a2 = 10;

        /**
         * Double   = double
         * Float    = float
         * Long     = long
         * Integer  = int
         * Short    = short
         * Byte     = byte
         * Boolean  = boolean
         * Character= char
         *
         * O String não tem tipo primitivo 
         * */

    }
}
